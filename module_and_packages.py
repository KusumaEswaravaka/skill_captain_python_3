import math_operations

#addition 
result_add = math_operations.add(10 , 5)
print(f"The addition of 10 and 5 is: {result_add}")


#subtarction 
result_subtract = math_operations.subtract(8, 4)
print(f"The subtraction of  4 from 8 is: {result_subtract}")

#Multiplication
result_multiply = math_operations.multiply(4, 3)
print(f"The multiplication of 4 and 3 is: {result_multiply}")

#division
result_divide = math_operations.divide(100, 10)
print(f"The division of 100 by 10: {result_divide}")

#power
result_power = math_operations.power(5, 2)
print(f"The result of 5 raised to the power of 2 is: {result_power}")

